def extract_keywords(text):
    """【面试题·任务二·1&2】关键词提取主函数

    输入示例："店长，我想喝点清甜水果味的，但不要加牛奶，推荐一下。"

    处理流程：
    1. jieba 分词 → ["店长","，","我","想","喝","点","清甜","水果味","的","，","但","不要","加","牛奶","，","推荐","一下","。"]
    2. 遍历 tokens：
       - 去停用词（我/想/喝/点/的/推荐/一下 → 丢弃）
       - 普通实词（清甜/水果味）→ 同义词扩展 → 归入 positive（正向词）
       - 否定前缀（不要）→ 跳过连接动词（加）→ 抓名词（牛奶）→ 归入 negative（负向词）
    3. 返回 (positive, negative) 两组词

    输出示例：
      positive = ["清爽","甘甜","轻甜","清甜","果香","水果","莓果","柑橘","柠檬"]  ← 用于 LIKE 召回
      negative = ["牛奶","奶","拿铁","奶香"]  ← 只用于 NOT LIKE 过滤

    【核心原则】负向词不参与召回，只用于过滤——这就是避开"否定词陷阱"的关键
    """
    import jieba

    tokens = [t for t in jieba.cut(text) if t.strip()]
    positive = []
    negative = []

    skip_words = STOP_WORDS | STOP_MARKS | NEGATIVE_PREFIXES | LINK_VERBS

    i = 0
    while i < len(tokens):
        token = tokens[i]

        # 【任务二·2】否定前缀处理：不/不要/无/没 + 跳过连接动词 + 抓名词 → 负向词
        if token in NEGATIVE_PREFIXES:
            i += 1
            # 跳过紧跟的连接动词（加/带/放...）—— 解决"不要加牛奶"中"加"被漏掉的问题
            while i < len(tokens) and tokens[i] in LINK_VERBS:
                i += 1
            # 收集后续名词，扩展同义词后归入 negative（负向词）
            # 遇到停用词/标点/下一个否定前缀即停止收集
            while i < len(tokens) and tokens[i] not in skip_words:
                negative.extend(SYNONYM_MAP.get(tokens[i], [tokens[i]]))
                i += 1
            continue

        # 【任务二·1】普通实词：去停用词后，扩展同义词，归入 positive（正向词）
        if token not in skip_words and len(token) > 1:
            positive.extend(SYNONYM_MAP.get(token, [token]))
        i += 1

    # 去重保序
    positive = list(dict.fromkeys(positive))
    negative = list(dict.fromkeys(negative))
    return positive, negative


