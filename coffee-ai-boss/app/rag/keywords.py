# ============================================================
# 【面试题·任务二·1&2】关键词提取模块
#
# 对应面试题：
#   任务二·1：如何提取关键词去匹配资料？
#     → jieba 分词 + 去停用词 + 同义词扩展
#   任务二·2：如何避开"否定词陷阱"（不要加牛奶）？
#     → 否定前缀 + 跳过连接动词 + 抓名词 → 负向词
#     → 负向词不参与召回，只用于过滤（NOT LIKE）
# ============================================================

from __future__ import annotations

# 【任务二·2】否定前缀词：出现这些词时，其后的实词视为「不想要」
# 例如 "不要" + "加" + "牛奶" → "牛奶" 是用户不想要的成分
NEGATIVE_PREFIXES = {
    "不", "不加", "不要", "不用", "无", "没有", "没", "拒绝", "别", "少",
}

# 【任务二·2】否定前缀后可能出现的连接动词：要跳过，才能抓到真正的名词
# 例如「不要加牛奶」→ 不(要) + 加(跳过) + 牛奶(名词才是真正想否定的)
# 如果不跳过"加"，就会因为"加"是单字(len=1)被丢掉，导致整个否定短语漏判
LINK_VERBS = {
    "加", "带", "放", "配", "给", "拌", "要", "用", "入", "倒",
}

# 转折词 / 标点：遇到即停止当前否定短语或正向词的收集
STOP_MARKS = {
    "，", "。", "、", "！", "？", "；", "：", "但", "但是", "可是", "不过", "然而",
    "而且", "并且", "及", "和", "与",
}

# 【任务二·1】停用词：分词后无意义、不参与检索
# 去掉这些词后，剩下的实词才是有效关键词
STOP_WORDS = {
    "我", "想", "要", "喝", "点", "的", "了", "吧", "一下", "推荐", "店长",
    "给", "来", "杯", "种", "帮", "帮忙", "介绍", "下", "有", "什么", "哪些",
    "和", "与", "及", "或", "是", "在", "也", "都", "还", "就", "那", "这",
    "一", "点", "些", "感觉", "觉得", "我想", "喝点", "想要", "帮我", "请问",
    "稍微", "比较", "有点", "味道", "口味", "口味", "偏", "多", "少",
}

# 【任务二·1】同义词/近义词扩展表：命中任一键，则展开为对应的全部词
# 作用：提高召回率。用户说"清甜"，手册里写的是"清爽"，也能匹配到
SYNONYM_MAP = {
    "清甜": ["清爽", "甘甜", "轻甜", "清甜"],
    "水果味": ["果香", "水果", "莓果", "柑橘", "柠檬", "热带水果"],
    "水果": ["果香", "水果", "莓果", "柑橘", "柠檬"],
    "果味": ["果香", "水果", "莓果", "柑橘"],
    "果香": ["果香", "水果", "莓果", "柑橘"],
    "牛奶": ["牛奶", "奶", "拿铁", "奶香"],
    "奶": ["牛奶", "奶", "拿铁", "奶香"],
    "苦": ["苦", "回甘"],
    "甜": ["甜", "焦糖", "糖"],
    "椰": ["椰香", "椰子", "椰乳"],
    "椰香": ["椰香", "椰子", "椰乳"],
    "冷萃": ["冷萃", "低温"],
}


def extract_keywords(text):
    """【面试题·任务二·1&2】关键词提取主函数

    输入示例："店长，我想喝点清甜水果味的，但不要加牛奶，推荐一下。"

    处理流程：
    1. jieba 分词 → ["店长","，","我","想","喝","点","清甜","水果味","的","，","但","不要","加","牛奶","，","推荐","一下","。"]
    2. 遍历 tokens：
       - 去停用词（我/想/喝/点/的/推荐/一下 → 丢弃）
       - 普通实词（清甜/水果味）→ 同义词扩展 → 归入 positive（正向词）
       - 否定前缀（不要）→ 跳过连接动词（加）→ 抓名词（牛奶）→ 归入 negative（负向词）
    3. 返回 (positive, negative) 两组词

    输出示例：
      positive = ["清爽","甘甜","轻甜","清甜","果香","水果","莓果","柑橘","柠檬"]  ← 用于 LIKE 召回
      negative = ["牛奶","奶","拿铁","奶香"]  ← 只用于 NOT LIKE 过滤

    【核心原则】负向词不参与召回，只用于过滤——这就是避开"否定词陷阱"的关键
    """
    import jieba

    tokens = [t for t in jieba.cut(text) if t.strip()]
    positive = []
    negative = []

    skip_words = STOP_WORDS | STOP_MARKS | NEGATIVE_PREFIXES | LINK_VERBS

    i = 0
    while i < len(tokens):
        token = tokens[i]

        # 【任务二·2】否定前缀处理：不/不要/无/没 + 跳过连接动词 + 抓名词 → 负向词
        if token in NEGATIVE_PREFIXES:
            i += 1
            # 跳过紧跟的连接动词（加/带/放...）—— 解决"不要加牛奶"中"加"被漏掉的问题
            while i < len(tokens) and tokens[i] in LINK_VERBS:
                i += 1
            # 收集后续名词，扩展同义词后归入 negative（负向词）
            # 遇到停用词/标点/下一个否定前缀即停止收集
            while i < len(tokens) and tokens[i] not in skip_words:
                negative.extend(SYNONYM_MAP.get(tokens[i], [tokens[i]]))
                i += 1
            continue

        # 【任务二·1】普通实词：去停用词后，扩展同义词，归入 positive（正向词）
        if token not in skip_words and len(token) > 1:
            positive.extend(SYNONYM_MAP.get(token, [token]))
        i += 1

    # 去重保序
    positive = list(dict.fromkeys(positive))
    negative = list(dict.fromkeys(negative))
    return positive, negative


if __name__ == "__main__":
    demo = "店长，我想喝点清甜水果味的，但不要加牛奶，推荐一下。"
    print(extract_keywords(demo))
    # 预期：positive=[清甜,甘甜,...,果香,柑橘,...] negative=[牛奶,奶,拿铁,奶香]
